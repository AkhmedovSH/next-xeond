    window.addEventListener('load', function(){
        setInterval(function(){
            const currentDate = new Date();
            const nextMonth = new Date(currentDate.getFullYear(), currentDate.getMonth() + 1, 1);
            const timeUntilNextMonth = nextMonth - currentDate;
            document.querySelector('#day').innerHTML = Math.floor(timeUntilNextMonth / (1000 * 60 * 60 * 24));
            document.querySelector('#hour').innerHTML = Math.floor((timeUntilNextMonth % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
            document.querySelector('#minut').innerHTML = Math.floor((timeUntilNextMonth % (1000 * 60 * 60)) / (1000 * 60));
            document.querySelector('#second').innerHTML = Math.floor((timeUntilNextMonth % (1000 * 60)) / 1000);
        }, 0)
    })

    const accardionToggle = (slideMenu) => (e) => {
        slideMenu.forEach((links) => {
            const hidePanel = links.nextElementSibling;
            if (links === e.currentTarget) {
                e.currentTarget.classList.toggle('active');
                hidePanel.classList.toggle('active-block');
            } else {
                links.classList.remove('active');
                hidePanel.classList.remove('active-block');
            }
        });
    };
    const slideMenu = document.querySelectorAll('.accardion-link');

    slideMenu.forEach((links) => {
        links.addEventListener('click', accardionToggle(slideMenu))
    });


